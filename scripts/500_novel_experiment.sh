#!/usr/bin/env bash
#
# 500-Novel Romantic Language Timeline: Large-Scale RLM Experiment
#
# Downloads ~421 novels (up to 50 per decade, 1800-1900) from Project Gutenberg,
# organized by decade in subdirectories, and runs an RLM analysis that
# tracks how romantic language evolves across the century.
#
# Corpus built programmatically by build_500_corpus.py via the Gutendex API.
# Early decades (1800s-1820s) have fewer novels due to Gutenberg catalog limits.
#
# Usage:
#   ./scripts/500_novel_experiment.sh
#   ./scripts/500_novel_experiment.sh --model gpt-5.4 --provider openai
#   ./scripts/500_novel_experiment.sh --dry-run
#
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
RLM_DIR="$(dirname "$SCRIPT_DIR")"
DATA_DIR="/tmp/rlm_500_novel_experiment"
RESULTS_DIR="$RLM_DIR/scripts/500_novel_results"

MODEL=""
PROVIDER=""
EXTRA_ARGS=()
DRY_RUN=false

while [[ $# -gt 0 ]]; do
    case "$1" in
        --model)     MODEL="$2"; shift 2 ;;
        --provider)  PROVIDER="$2"; shift 2 ;;
        --dry-run)   DRY_RUN=true; shift ;;
        *)           EXTRA_ARGS+=("$1"); shift ;;
    esac
done

# ── Novel corpus (auto-generated by build_500_corpus.py) ────────────────────
source "$SCRIPT_DIR/500_novel_corpus.sh"

# ── Download ─────────────────────────────────────────────────────────────────

echo "=== Downloading ${#NOVELS[@]} novels to $DATA_DIR ==="

downloaded=0
skipped=0
failed=0

for entry in "${NOVELS[@]}"; do
    decade=$(echo "$entry" | awk '{print $1}')
    id=$(echo "$entry" | awk '{print $2}')
    filename=$(echo "$entry" | awk '{print $3}')
    dest_dir="$DATA_DIR/$decade"
    dest="$dest_dir/$filename"

    mkdir -p "$dest_dir"

    if [[ -f "$dest" ]]; then
        size=$(wc -c < "$dest" | tr -d ' ')
        if [[ "$size" -gt 1000 ]]; then
            skipped=$((skipped + 1))
            continue
        fi
        # Too small — likely a failed download, retry
        rm -f "$dest"
    fi

    url="https://www.gutenberg.org/cache/epub/${id}/pg${id}.txt"
    if curl -sS -L --fail -o "$dest" "$url" 2>/dev/null; then
        size=$(wc -c < "$dest" | tr -d ' ')
        if [[ "$size" -gt 1000 ]]; then
            downloaded=$((downloaded + 1))
            echo "  [ok]    $decade/$filename (${size} bytes)"
        else
            echo "  [WARN]  $decade/$filename too small (${size} bytes), removing" >&2
            rm -f "$dest"
            failed=$((failed + 1))
        fi
    else
        echo "  [WARN]  Failed to download $decade/$filename (ID $id)" >&2
        rm -f "$dest"
        failed=$((failed + 1))
    fi
done

total_bytes=$(du -sh "$DATA_DIR" | awk '{print $1}')
file_count=$(find "$DATA_DIR" -name "*.txt" | wc -l | tr -d ' ')

echo ""
echo "=== Download complete ==="
echo "  New downloads: $downloaded"
echo "  Already cached: $skipped"
echo "  Failed: $failed"
echo "  Total files: $file_count, $total_bytes"
echo ""

# Show per-decade breakdown
for decade_dir in "$DATA_DIR"/*/; do
    decade=$(basename "$decade_dir")
    count=$(ls "$decade_dir"/*.txt 2>/dev/null | wc -l | tr -d ' ')
    size=$(du -sh "$decade_dir" | awk '{print $1}')
    echo "  $decade: $count novels, $size"
done
echo ""

if $DRY_RUN; then
    echo "(dry-run mode — skipping RLM execution)"
    exit 0
fi

# ── Run experiment ───────────────────────────────────────────────────────────

QUERY="Analyze the evolution of romantic language across ~420 novels spanning 1800-1900. \
The novels are organized by decade in subdirectories (1800s/ through 1890s/). \
Early decades (1800s, 1810s, 1820s) have fewer novels; later decades have ~50 each.

For each novel:
1. Identify and extract passages containing romantic language (courtship, \
declarations of love, descriptions of attraction, romantic suffering, \
marriage proposals, etc.)
2. Compute metrics: frequency of romantic passages per 10,000 words, \
average sentiment intensity (1-5 scale), and the dominant romantic \
themes present.

Then synthesize across all novels:
- Build a decade-by-decade timeline showing how romantic language frequency, \
sentiment intensity, and dominant themes shift from 1800 to 1900.
- Identify the inflection points: which decades show the biggest changes \
and why?
- Compare early decades (smaller sample) vs later decades (larger sample) — \
are the patterns consistent?
- Name the top 10 most romantically intense passages across the entire corpus \
with exact quotes (include decade, novel title, and author).

Return structured JSON with per-novel data, per-decade aggregates, the \
timeline, inflection analysis, and the top passages."

mkdir -p "$RESULTS_DIR"
timestamp=$(date +%Y%m%d_%H%M%S)
result_file="$RESULTS_DIR/run_${timestamp}.json"
transcript_file="$RESULTS_DIR/run_${timestamp}_transcript.json"
log_file="$RESULTS_DIR/run_${timestamp}.log"

CLI_ARGS=(
    -q "$QUERY"
    --context-dir "$DATA_DIR"
    --verbose
    --json
    --max-iterations 30
    --save-transcript "$transcript_file"
)

if [[ -n "$MODEL" ]]; then
    CLI_ARGS+=(--model "$MODEL")
fi
if [[ -n "$PROVIDER" ]]; then
    CLI_ARGS+=(--provider "$PROVIDER")
fi
CLI_ARGS+=("${EXTRA_ARGS[@]}")

echo "=== Running 500-Novel Romantic Timeline experiment ==="
echo "  Model:      ${MODEL:-default}"
echo "  Provider:   ${PROVIDER:-default}"
echo "  Data:       $DATA_DIR ($file_count novels, 10 decades)"
echo "  Max iter:   30"
echo "  Result:     $result_file"
echo "  Transcript: $transcript_file"
echo "  Log:        $log_file"
echo ""

cd "$RLM_DIR"
uv run rlm/cli.py "${CLI_ARGS[@]}" \
    > "$result_file" \
    2> >(tee "$log_file" >&2)

echo ""
echo "=== Experiment complete ==="
echo "  Result:     $result_file"
echo "  Transcript: $transcript_file"
echo "  Log:        $log_file"
